-- Show number in the gutter
vim.opt.number = true

-- Allows to use mouse
vim.opt.mouse = 'a'

-- Ignore uppercase on search
vim.opt.ignorecase = true

-- Search ignore uppercase letter unless the search has an uppercase letter
vim.opt.smartcase = true

-- Remove Higlights in the results of the previus search
vim.opt.hlsearch = false

-- Put this option in false if you want to make the text always visible
vim.opt.wrap = true

-- Tabs options
vim.opt.tabstop = 2
vim.opt.shiftwidth = 2
vim.opt.expandtab = false


